# Mixins (traits em php)

class FalarMixin:
    def falar(self):
        return "Eu estou falando"
    
class Pato(Ave, FalarMixin):
    def __init__(self, cor_bico, nro_patas):
        super().__init__(cor_bico=cor_bico, nro_patas=nro_patas)

pato = Pato("amarelo", 2)
print(pato.falar())




